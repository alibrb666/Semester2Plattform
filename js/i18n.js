// Language as it was when the page first loaded — before any user interaction.
// Used to detect whether the user explicitly changed the language on the profile screen.
export const initialLanguage = _lang;

function normalize(lang) {
  return ['de', 'en', 'fr'].includes(lang) ? lang : 'de';
}

export function setLanguage(lang) {
  _lang = normalize(lang);
  localStorage.setItem(LANG_KEY, _lang);
  document.documentElement.lang = _lang;
  document.dispatchEvent(new CustomEvent('i18n:changed', { detail: { lang: _lang } }));
}

// Like setLanguage but does not dispatch i18n:changed — use when you need to
// update the language before a manual refreshCurrentView() to avoid double renders.
export function setLanguageSilent(lang) {
  _lang = normalize(lang);
  localStorage.setItem(LANG_KEY, _lang);
  document.documentElement.lang = _lang;
}

export function getLanguage() {
  return _lang;
}

export function t(key) {
  return DICT[_lang]?.[key] || DICT.de[key] || key;
}

export function translateDom(root = document) {
  if (_lang === 'de' || !root) return;
  const phrases = PHRASES[_lang] || {};
  const sorted = Object.keys(phrases).sort((a, b) => b.length - a.length);
  const translateText = value => {
    if (!value) return value;
    const trimmed = value.trim();
    if (phrases[trimmed]) return value.replace(trimmed, phrases[trimmed]);
    let out = value;
    sorted.forEach(source => {
      if (!out.includes(source)) return;
      out = out.split(source).join(phrases[source]);
    });
    return out;
  };
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const text = node.nodeValue.trim();
      if (!text) return NodeFilter.FILTER_REJECT;
      const parent = node.parentElement;
      if (parent?.closest('script,style,code,kbd,input,textarea')) return NodeFilter.FILTER_REJECT;
      if (translateText(node.nodeValue) === node.nodeValue) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach(node => { node.nodeValue = translateText(node.nodeValue); });
  root.querySelectorAll?.('[aria-label],[data-tooltip],[title],input[placeholder],textarea[placeholder]').forEach(el => {
    ['aria-label', 'data-tooltip', 'title', 'placeholder'].forEach(attr => {
      const val = el.getAttribute(attr);
      if (val) el.setAttribute(attr, translateText(val));
    });
  });
}

